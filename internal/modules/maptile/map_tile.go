package maptile

import (
	"app/internal/modules/geom"

	"github.com/hajimehoshi/ebiten/v2"
)

const (
	Empty     MapTile = 0
	Wall      MapTile = 1
	WaterTire MapTile = 2
	WaterVase MapTile = 3
	EmptyTire MapTile = 4
)

type (
	MapTile int

	Map struct {
		Tiles  [][]int
		Images map[MapTile]*ebiten.Image
	}
)

func New() (*Map, error) {
	images, err := loadImageTiles()
	if err != nil {
		return nil, err
	}
	return &Map{
		Tiles:  loadMap(),
		Images: images,
	}, nil
}

func (m *Map) GetTileAt(position geom.Position) MapTile {
	if position.Y < 0 || position.Y >= len(m.Tiles) || position.X < 0 || position.X >= len(m.Tiles[0]) {
		return Wall
	}
	return MapTile(m.Tiles[position.Y][position.X])
}

